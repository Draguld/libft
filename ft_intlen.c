#include "libft.h"

int		ft_intlen(int nb)
{
	int		len;

	len = 1;
	if (nb < 0)
		nb = nb * -1;
	while (nb >= 10)
	{
		nb = nb / 10;
		len++;
	}
	return (len);
}
