#include "libft.h"

char	*ft_strcpy(char *dest, const char *src)
{
	int		i;

	i = 0;
	while (*src != '\0')
	{
		*dest++ = *src++;
		i++;
	}
	*dest = '\0';
	return (dest = dest - i);
}
