#include "libft.h"

char	*ft_strncpy(char *dest, const char *src, size_t n)
{
	int		i;

	i = 0;
	while (n != 0 && *src != '\0')
	{
		*dest = *src;
		dest++;
		src++;
		n--;
		i++;
	}
	while (n-- > 0)
	{
		*dest = '\0';
		dest++;
		i++;
	}
	return (dest = dest - i);
}
